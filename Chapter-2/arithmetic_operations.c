#include<stdio.h>
int main()
{
    int x = 500;
    int y = 300;

    //addition
    printf("%d \n",x+y);

    //subtraction
    printf("%d \n",x-y);

    //multiplication
    printf("%d \n",x*y);

    //division
    printf("%d \n",x/y);

    //remainder
    printf("%d \n",x%y);
    
    //another way
    int z= x+y;
    printf("%d",z);
    
    return 0;
}